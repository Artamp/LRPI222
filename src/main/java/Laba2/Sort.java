package Laba2;

public class Sort {

	public static int[] sorter(int[] array) {
		
		
		int size = array.length;
		
		
		for (int i = 0; i < size;i++) {
			for (int j = 0; j < size-1;j++) {
				if (array[j] > array[j+1]) {
					int c = array[j];
					array[j] = array[j+1];
					array[j+1] = c;
				} 
			}
		}
	
		
		return array;
		
	}
}
